package com.yolanda.conditionals.and.loops;

public class SwitchStatements {
    public static void main(String[] args) {
        int x = 10;

       switch (x) {

            case 10:

                System.out.println("A");
                break;

            case 20:

            System.out.println("B");
            break;

        }
    }
}
